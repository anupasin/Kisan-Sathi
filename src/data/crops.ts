/** Rank crops for a given season + soil texture. */
export function recommendCrops(
  season: Season,
  textureKey: string,
): { recommended: Crop[]; others: Crop[] } {
  const inSeason = CROPS.filter((c) => c.seasons.includes(season));
  const recommended = inSeason.filter((c) => c.soils.includes(textureKey));
  const others = inSeason.filter((c) => !c.soils.includes(textureKey));
  return { recommended, others };
}
